function [h, kp, kr] = f_PR_controller(s0, a, b, c, z)

    % Calculate h
    h  = (2 .* b .* z .^ 2 + 2 .* a .* z + 2) ./ (-s0 .^ 3 .* z .^ 2 + b .* s0 .* z .^ 2 + a .* s0 .* z + 3 .* s0 .^ 2 .* z - b .* z - a - 2 .* s0);

    % Calculate kp, and kr
    kr = -(s0 .^ 4 .* z .^ 2 - 2 .* b .* s0 .^ 2 .* z .^ 2 - 2 .* a .* s0 .^ 2 .* z + b .^ 2 .* z .^ 2 - 4 .* s0 .^ 3 .* z + 2 .* a .* b .* z + 4 .* b .* s0 .* z + a .^ 2 + 4 .* a .* s0 + 4 .* s0 .^ 2) ./ exp(-(h .* s0)) ./ c ./ (b .* s0 .* z .^ 3 + a .* s0 .* z .^ 2 - b .* z .^ 2 - a .* z + s0 .* z - 1) ./ 0.2e1;
    kp = ((-s0 .^ 4 .* z .^ 2 + 2 .* a .* b .* s0 .* z .^ 2 + 4 .* b .* s0 .^ 2 .* z .^ 2 + 2 .* a .^ 2 .* s0 .* z + 4 .* a .* s0 .^ 2 .* z + b .^ 2 .* z .^ 2 + 4 .* s0 .^ 3 .* z - 4 .* b .* s0 .* z - a .^ 2 - 2 .* a .* s0 - 2 .* s0 .^ 2 + 2 .* b) ./ c ./ (s0 .* z - 1) ./ (b .* z .^ 2 + a .* z + 1)) ./ 0.2e1;

end